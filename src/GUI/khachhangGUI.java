    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
         if (jtmakhachhang.getText().trim().equals("") || jttenkhachhang.getText().trim().equals("")
                || cb.getSelectedItem() == " " || jtdiachi.getText().trim().equals("")
                || jtsdt.getText().trim().equals("")) {
            JOptionPane.showMessageDialog(this, "Vui lòng nhập đủ thông tin");
        } else {
            khachhangDTO khachhangdto = new khachhangDTO();
            khachhangdto.setMakhachhang(jtmakhachhang.getText());
            khachhangdto.setTenkhachhang(jttenkhachhang.getText());
            khachhangdto.setGioitinh(cb.getSelectedItem().toString());
            khachhangdto.setDiachi(jtdiachi.getText());
            khachhangdto.setSdt(jtsdt.getText());

            khachhangBLL khachhangbll = new khachhangBLL();
            try {
                int result = khachhangbll.insertkhachhang(khachhangdto);
                if (result != 0) {
                    JOptionPane.showMessageDialog(this, "Thêm khách hàng thành công!", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
                    loadkhachhangList();
                } else {
                    JOptionPane.showMessageDialog(this, "Thêm khách hàng thất bại!", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
                }

            } catch (Exception ex) {
                ex.printStackTrace();
            }
        }
    }//GEN-LAST:event_btnThemActionPerformed

