    public void loadkhachhangList() {
        dskh.setRowCount(0);
        ArrayList<khachhangDTO> khachhangarr = new ArrayList<>();
        khachhangBLL khachhangbll = new khachhangBLL();
        khachhangarr = khachhangbll.getAllkhachhang();
        khachhangDTO khachhangdto = new khachhangDTO();
        for (int i = 0; i < khachhangarr.size(); i++) {
            khachhangdto = khachhangarr.get(i);
            String makhachhang = khachhangdto.getMakhachhang();
            String tenkhachhang = khachhangdto.getTenkhachhang();
            String gioitinh = khachhangdto.getGioitinh();
            String diachi = khachhangdto.getDiachi();
            String sdt = khachhangdto.getSdt();
          
            Object[] row = {makhachhang, tenkhachhang, gioitinh, diachi, sdt};
            dskh.addRow(row);
        }
        jtldskhachhang.setModel(dskh);
    }
