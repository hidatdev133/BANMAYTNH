    public void loadSanphamList() {
        dtm.setRowCount(0);
        ArrayList<sanphamDTO> sanphamarr = new ArrayList<>();
        sanphamBLL sanphambll = new sanphamBLL();
        sanphamarr = sanphambll.getAllsanpham();
        sanphamDTO sanphamdto = new sanphamDTO();
        for (int i = 0; i < sanphamarr.size(); i++) {
            sanphamdto = sanphamarr.get(i);
            int masanpham = sanphamdto.getMasanpham();
            String tensanpham = sanphamdto.getTensanpham();
            String loaisanpham = sanphamdto.getLoaisanpham();
            int giasanpham = sanphamdto.getGiasanpham();
            String nhacungcap = sanphamdto.getNhacungcap();
            int thoigianbaohanh = sanphamdto.getThoigianbaohanh();
            Object[] row = {masanpham, tensanpham, loaisanpham, giasanpham, nhacungcap, thoigianbaohanh};
            dtm.addRow(row);
        }
        jtlDSSanPham.setModel(dtm);
    }
    
    private void DSNCC() {
        dsncc.setRowCount(0);

        ArrayList<nhacungcapDTO> nhacungcaparr = new ArrayList<>();
        nhacungcapBLL nhacungcapbll = new nhacungcapBLL();
        nhacungcaparr = nhacungcapbll.getAllnhacungcap();
        nhacungcapDTO nhacungcapdto = new nhacungcapDTO();
        for (int i = 0; i < nhacungcaparr.size(); i++) {
            nhacungcapdto = nhacungcaparr.get(i);
            String manhacungcap = nhacungcapdto.getManhacungcap();
            String tennhacungcap = nhacungcapdto.getTennhacungcap();
            String diachi = nhacungcapdto.getDiachi();
            String sdt = nhacungcapdto.getSdt();
            Object[] row = {manhacungcap, tennhacungcap, diachi,  sdt};
            dsncc.addRow(row);
        }
        jtldsncc.setModel(dsncc);
    }
    
