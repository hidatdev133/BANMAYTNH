    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        if (jtMaSanPham.getText().trim().equals("") || jtTenSanPham.getText().trim().equals("")
                || jtGiaSanPham.getText().trim().equals("") || jtNhaCungCap.getText().trim().equals("")
                || jtThoiGianBaoHanh.getText().trim().equals("")|| cb.getSelectedItem() == " ") {
            JOptionPane.showMessageDialog(this, "Vui lòng nhập đủ thông tin");
        } else {
            sanphamDTO sanphamdto = new sanphamDTO();
            sanphamdto.setMasanpham(Integer.parseInt(jtMaSanPham.getText()));
            sanphamdto.setTensanpham(jtTenSanPham.getText());
            sanphamdto.setLoaisanpham(cb.getSelectedItem().toString());
            sanphamdto.setGiasanpham(Integer.parseInt(jtGiaSanPham.getText()));
            sanphamdto.setNhacungcap(jtNhaCungCap.getText());
            sanphamdto.setThoigianbaohanh(Integer.parseInt(jtThoiGianBaoHanh.getText()));

            sanphamBLL sanphambll = new sanphamBLL();
            try {
                int result = sanphambll.insertsanpham(sanphamdto);
                if (result != 0) {
                    JOptionPane.showMessageDialog(this, "Thêm sản phẩm thành công!", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
                    loadSanphamList();
                } else {
                    JOptionPane.showMessageDialog(this, "Sản phẩm đã tồn tại!", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
                }

            } catch (Exception ex) {
                ex.printStackTrace();
            }
        }
    }//GEN-LAST:event_btnThemActionPerformed

